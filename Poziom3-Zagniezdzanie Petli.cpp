#include <iostream>

using namespace std;
 
int main() 
{
    
    for (int x=1; x<4; x++) 
    
    {
        for (int y=1; y<4; y++) 
        
        {
            
            for (int n=1; n<4; n++)
            
            {
                
                for (int i=1; i<4; i++)
                
                {
                    
                    for (int z=1; z<4; z++)
                    
                    {
                        
                        cout << x << " " << y << " " << n << " " << i << " " << z << endl;
                        
                    }
                }
            }
        }
    }
 
    return 0;
}
